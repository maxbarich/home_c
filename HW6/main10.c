#include <stdio.h>

// Функция печати простых множителей
void print_simple(int n) {
    if (n <= 1) return; // Простых множителей нет
    
    // Обрабатываем делитель 2 отдельно
    while (n % 2 == 0) {
        printf("2 ");
        n /= 2;
    }
    
    // Проверяем нечётные делители от 3 до sqrt(n)
    for (int i = 3; i * i <= n; i += 2) {
        while (n % i == 0) {
            printf("%d ", i);
            n /= i;
        }
    }
    
    // Если осталось простое число больше 2
    if (n > 2) {
        printf("%d ", n);
    }
}

int main() {
    int n;
    scanf("%d", &n); // Вводим число
    
    // Проверяем корректность ввода
    if (n <= 0) {
        printf("Число должно быть положительным\n");
        return 1;
    }
    
    // Печатаем простые множители
    print_simple(n);
    printf("\n"); // Переход на новую строку
    
    return 0;
}
