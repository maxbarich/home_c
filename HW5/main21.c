#include <stdio.h>

int main() {
    char c;
    
    while (1) {
        c = getchar(); // Читаем по одному символу
        
        if (c == '.') { // Если встретили точку - завершаем обработку
            break;
        }
        
        // Если символ - заглавная буква (A-Z), преобразуем в строчную
        if (c >= 'A' && c <= 'Z') {
            c = c + ('a' - 'A'); // или c = c + 32
        }
        
        putchar(c); // Выводим символ
    }
    
    return 0;
}
