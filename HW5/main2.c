#include <stdio.h>

int main() {
    int a, b;
    scanf("%d %d", &a, &b);  // Вводим два числа a и b
    
    // Проверяем, что a ≤ b (по условию задачи)
    if (a > b) {
        printf("Ошибка: a должно быть меньше или равно b\n");
        return 1;  // Завершаем программу с ошибкой
    }
    
    // Выводим квадраты чисел от a до b
    for (int i = a; i <= b; i++) {
        printf("%d", i * i);  // Выводим квадрат текущего числа
        
        // Если это не последнее число, добавляем пробел
        if (i < b) {
            printf(" ");
        }
    }
    printf("\n");  // Переход на новую строку в конце
    
    return 0;
}
