#include <stdio.h>

int main() {
    int number;
    scanf("%d", &number); // Вводим натуральное число
    
    // Проверяем, что число положительное (по условию - натуральное)
    if (number <= 0) {
        printf("Число должно быть натуральным (положительным)\n");
        return 1;
    }
    
    int min = 9;  // Начальное значение для минимальной цифры
    int max = 0;  // Начальное значение для максимальной цифры
    
    while (number > 0) {
        int digit = number % 10; // Получаем текущую цифру
        
        // Обновляем минимальное значение
        if (digit < min) {
            min = digit;
        }
        
        // Обновляем максимальное значение
        if (digit > max) {
            max = digit;
        }
        
        number /= 10; // Удаляем последнюю цифру
    }
    
    printf("%d %d\n", min, max); // Выводим результат
    
    return 0;
}
