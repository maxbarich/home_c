#include <stdio.h>

int main() {
    int n;
    scanf("%d", &n); // Вводим количество элементов
    
    // Проверяем, что n натуральное число
    if (n <= 0) {
        printf("Число должно быть натуральным (положительным)\n");
        return 1;
    }
    
    // Первые два числа Фибоначчи
    long long a = 1, b = 1;
    
    // Обработка особых случаев
    if (n == 1) {
        printf("1");
    } else if (n == 2) {
        printf("1 1");
    } else {
        printf("1 1"); // Выводим первые два числа
        
        // Вычисляем и выводим остальные числа
        for (int i = 3; i <= n; i++) {
            long long next = a + b;
            printf(" %lld", next);
            
            // Обновляем значения для следующей итерации
            a = b;
            b = next;
        }
    }
    
    printf("\n"); // Переход на новую строку в конце
    return 0;
}
