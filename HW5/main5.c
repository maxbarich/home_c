#include <stdio.h>

int main() {
    int number;
    scanf("%d", &number);  // Вводим целое число
    
    // Проверяем, что число неотрицательное (по условию задачи)
    if (number < 0) {
        printf("Число должно быть неотрицательным\n");
        return 1;  // Завершаем программу с ошибкой
    }
    
    int sum = 0;  // Переменная для хранения суммы цифр
    
    // Вычисляем сумму цифр числа
    while (number > 0) {
        sum += number % 10;  // Добавляем последнюю цифру к сумме
        number /= 10;        // Удаляем последнюю цифру из числа
    }
    
    printf("%d\n", sum);  // Выводим результат
    
    return 0;
}
