#include <stdio.h>

int main() {
    int a, b;
    scanf("%d %d", &a, &b);  // Ввод двух чисел
    
    // Проверка условия a ≤ b (по условию задачи)
    if (a > b) {
        printf("Ошибка: a должно быть меньше или равно b\n");
        return 1;  // Завершение с ошибкой
    }
    
    int sum = 0;  // Переменная для хранения суммы квадратов
    
    // Вычисляем сумму квадратов от a до b
    for (int i = a; i <= b; i++) {
        sum += i * i;  // Добавляем квадрат текущего числа к сумме
    }
    
    printf("%d\n", sum);  // Выводим результат
    
    return 0;
}
