#include <stdio.h>

int main() {
    int number;
    scanf("%d", &number); // Вводим целое неотрицательное число
    
    // Проверяем, что число неотрицательное (по условию задачи)
    if (number < 0) {
        printf("Число должно быть неотрицательным\n");
        return 1;
    }
    
    int reversed = 0; // Здесь будем хранить "перевёрнутое" число
    
    // Переворачиваем число
    while (number > 0) {
        int digit = number % 10; // Получаем последнюю цифру
        reversed = reversed * 10 + digit; // Добавляем её к перевёрнутому числу
        number /= 10; // Удаляем последнюю цифру из исходного числа
    }
    
    printf("%d\n", reversed); // Выводим результат
    
    return 0;
}
