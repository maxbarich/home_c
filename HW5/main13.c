#include <stdio.h>

int main() {
    int number;
    scanf("%d", &number); // Вводим целое неотрицательное число
    
    // Проверяем, что число неотрицательное
    if (number < 0) {
        printf("Число должно быть неотрицательным\n");
        return 1;
    }
    
    // Обрабатываем особый случай - число 0
    if (number == 0) {
        printf("1 0\n"); // 0 считается чётным
        return 0;
    }
    
    int even = 0; // Счётчик чётных цифр
    int odd = 0;  // Счётчик нечётных цифр
    
    while (number > 0) {
        int digit = number % 10; // Получаем последнюю цифру
        
        if (digit % 2 == 0) {
            even++; // Увеличиваем счётчик чётных
        } else {
            odd++;  // Увеличиваем счётчик нечётных
        }
        
        number /= 10; // Удаляем последнюю цифру
    }
    
    printf("%d %d\n", even, odd); // Выводим результат
    
    return 0;
}
