int compression(int a[], int b[], int N) {
    if (N == 0) return 0; // обработка пустого массива

    int count = 0;    // счетчик элементов в сжатом массиве
    int current = a[0]; // текущее значение серии (0 или 1)
    int length = 1;   // длина текущей серии

    // Если массив начинается с 1, записываем 0 в начало
    if (current == 1) {
        b[count++] = 0;
    }

    for (int i = 1; i < N; i++) {
        if (a[i] == current) {
            length++; // увеличиваем длину текущей серии
        } else {
            // Записываем длину завершенной серии
            b[count++] = length;
            // Начинаем новую серию
            current = a[i];
            length = 1;
        }
    }
    // Записываем длину последней серии
    b[count++] = length;

    return count; // возвращаем количество элементов в сжатом массиве
}
