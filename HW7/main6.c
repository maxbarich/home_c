#include <stdio.h>

// Рекурсивная функция для чтения и вывода строки в обратном порядке
void reverse_string() {
    char c = getchar(); // Читаем символ
    
    // Базовый случай: если встретили точку, завершаем рекурсию
    if (c == '.') {
        return;
    }
    
    // Рекурсивный вызов для чтения следующего символа
    reverse_string();
    
    // Выводим символ после возврата из рекурсии
    putchar(c);
}

int main() {
    reverse_string(); // Вызываем рекурсивную функцию
    printf("\n");     // Добавляем перевод строки в конце
    
    return 0;
}
