#include <stdio.h>

// Рекурсивная функция для вывода цифр в прямом порядке
void print_num(int num) {
    // Базовый случай: если число меньше 10
    if (num < 10) {
        printf("%d", num);
        return;
    }
    
    // Рекурсивно обрабатываем все цифры, кроме последней
    print_num(num / 10);
    
    // Выводим последнюю цифру с пробелом перед ней
    printf(" %d", num % 10);
}

int main() {
    int n;
    scanf("%d", &n); // Вводим число
    
    // Проверяем, что число неотрицательное
    if (n < 0) {
        printf("Число должно быть неотрицательным\n");
        return 1;
    }
    
    // Особый случай для нуля
    if (n == 0) {
        printf("0\n");
        return 0;
    }
    
    print_num(n); // Вызываем рекурсивную функцию
    printf("\n"); // Переход на новую строку
    
    return 0;
}
