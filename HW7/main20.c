#include <stdio.h>

int recurs_power(int n, int p) {
    if (p == 0) {
        return 1; // любое число в степени 0 равно 1
    }
    if (p < 0) {
        // В условии не указано, как обрабатывать отрицательную степень,
        // но так как результат может быть дробным, для целых чисел можно вернуть 0 или обработать отдельно.
        // Для простоты, считаем, что p >= 0.
        return 0; // или можно вывести ошибку
    }
    return n * recurs_power(n, p - 1);
}

