#include <stdio.h>

// Рекурсивная функция перевода в двоичную систему
void dec_to_bin(int n) {
    if (n > 1) {
        dec_to_bin(n / 2);  // Рекурсивный вызов для старших разрядов
    }
    printf("%d", n % 2);    // Вывод младшего бита
}

int main() {
    int n;
    scanf("%d", &n);  // Вводим число
    
    // Проверяем, что число неотрицательное
    if (n < 0) {
        printf("Число должно быть неотрицательным\n");
        return 1;
    }
    
    // Особый случай для нуля
    if (n == 0) {
        printf("0\n");
        return 0;
    }
    
    dec_to_bin(n);  // Вызываем рекурсивную функцию
    printf("\n");   // Переход на новую строку
    
    return 0;
}
