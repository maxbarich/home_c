#include <stdio.h>

// Рекурсивная функция для вывода цифр в обратном порядке
void reverse_digits(int n) {
    if (n < 10) {
        // Базовый случай: если число однозначное
        printf("%d", n);
    } else {
        // Рекурсивный случай: выводим последнюю цифру и обрабатываем оставшееся число
        printf("%d ", n % 10);
        reverse_digits(n / 10);
    }
}

int main() {
    int n;
    scanf("%d", &n); // Вводим число
    
    // Проверяем, что число неотрицательное
    if (n < 0) {
        printf("Число должно быть неотрицательным\n");
        return 1;
    }
    
    // Особый случай для нуля
    if (n == 0) {
        printf("0\n");
        return 0;
    }
    
    reverse_digits(n); // Вызываем рекурсивную функцию
    printf("\n"); // Переход на новую строку
    
    return 0;
}
